#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	int	count;

	count = 1;
	while (argc > 1 && count < argc)
	{
		ft_putstr(argv[count]);
		write(1, "\n", 1);
		count++;
	}
}
